    private void btLogInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btLogInActionPerformed
        // TODO add your handling code here:
        login user = new login();
        user.setUserID(tfUserID.getText());
        user.setPassword(tfPassword.getText());
        String UserID = user.getUserID();
        String Password = user.getPassword();
        
        try {
            // PrintWriter object for User.txt
            PrintWriter pw = new PrintWriter("User.txt");
            // BufferedRead object for Customer.txt
            BufferedReader br = new BufferedReader(new FileReader("Customer.txt"));
            String line;
            while ((line = br.readLine()) != null) {
                // loop to copy each line of Customer.txt to User.txt
                pw.println(line);
            }
            br = new BufferedReader(new FileReader("Admin.txt"));
            while ((line = br.readLine()) != null) {
                pw.println(line);
            }
            pw.flush();

            // closing resources
            br.close();
            pw.close();

        } catch (FileNotFoundException ex) {
            Logger.getLogger(UserLoginMenu.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(UserLoginMenu.class.getName()).log(Level.SEVERE, null, ex);
        }

        File file = new File("User.txt");
        try {
            FileReader fr = new FileReader(file);
            BufferedReader br = new BufferedReader(fr);
            boolean check = false;

            String line;
            while ((line = br.readLine()) != null) {
                String[] recLine = line.split(";");
                // Edited
                if (UserID.equals(recLine[1])) {
                    if (Password.equals(recLine[6])) {
                        String Name = recLine[2];
                        String userType = recLine[7]; // change from 5 to 7
                        if (userType.equals("customer")) {
                            JOptionPane.showMessageDialog(null, "Login Successful!");
                            SystemLoginActivity();
                            this.dispose();
                            new CustomerMenu(Name, UserID).setVisible(true);
                            check = true;
                            break;
                        } else if (userType.equals("admin")) {
                            JOptionPane.showMessageDialog(null, "Login Successful!");
                            SystemLoginActivity();
                            this.dispose();
                            new AdminMenu(Name).setVisible(true);
                            check = true;
                            break;
                        } else {
                            JOptionPane.showMessageDialog(null, "Invalid Credentials! Please Try Again!");
                        }
                    }
                }
            }
            if (check == false) {
                JOptionPane.showMessageDialog(null, "Invalid Login Credentials! Please Register Yourself First if You Do Not Have an Account!");
            }
        } catch (HeadlessException | IOException x) {
            JOptionPane.showMessageDialog(null, "Record Not Found..." + UserID);
        }        
    }//GEN-LAST:event_btLogInActionPerformed


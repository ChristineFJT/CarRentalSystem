    private void SystemLoginActivity() throws IOException {
        login user = new login();
        user.setUserID(tfUserID.getText());
        user.setPassword(tfPassword.getText());
        String UserID = user.getUserID();
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy/MM/d HH:mm:ss");
        Date date = new Date();
        String loginTime = sdf.format(date);
        
        File userInfo = new File("User.txt");
        File loginActivity = new File("SystemLog.txt");
        if(loginActivity.createNewFile()) {
            System.out.println("File has been created.");
        } else {
            System.out.println("File already exists.");
        }
        try {
            BufferedReader br = new BufferedReader(new FileReader(userInfo));
            FileWriter fw = new FileWriter(loginActivity, true);
            BufferedWriter bw = new BufferedWriter(fw);
            PrintWriter pw = new PrintWriter(bw);
            
            String line;
            while ((line = br.readLine()) != null) {
                String[] recLine = line.split(";");
                if (UserID.equals(recLine[1])) {
                    String Name = recLine[2];

                    pw.println(loginTime + ";" + UserID + ";" + Name + ";");
                }
            }
            
            pw.close();
            bw.close();
            fw.close();
        } catch (IOException ex) {
            Logger.getLogger(UserLoginMenu.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
